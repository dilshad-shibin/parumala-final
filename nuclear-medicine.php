                                    <p>The department is equipped with a gamma camera with SPECT facility (GE Brivo NM 615). Nuclear Medicine imaging using gamma camera is utilized to image functional / metabolic processes in various organs in the body. Unlike computed tomography (CT) or Magnetic Resonance Imaging (MRI) which are anatomic / morphological imaging, nuclear medicine imaging provides insight into functional aspects of various organs in the body. In most diseases, functional changes precede anatomic changes, so scintigraphic evidence of a disease process can be diagnosed at an earlier stage. Nuclear medicine procedures done using a gamma camera are usually non invasive and safe with a small amount of radiation exposure which is very well comparable with diagnostic radiology procedures.</p>
